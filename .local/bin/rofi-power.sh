#!/usr/bin/env bash
# Rofi Power Menu

# Виводимо пункти для dmenu
MENU=(" Перезавантажити систему" " Вимкнути систему")

# Якщо аргумент передано — обробляємо вибір
if [ -n "$1" ]; then
    case "$1" in
        " Перезавантажити систему") reboot ;;
        " Вимкнути систему") poweroff ;;
    esac
    exit
fi

# Якщо без аргументів — повертаємо список для rofi
printf '%s\n' "${MENU[@]}"
